### OPERADORES ###

print(3 + 4)
print(3 - 4)
print(3 * 4)
print(3 / 4)
print(10 % 3) # Modulo. Imprime el resto del resultado
print(10 // 3) # Division con numero entero como resultado
print(3 ** 4) # Exponente

print('Hola' + 'Mundo') # El + concatena las cadenas de textos


### OPERADORES COMPARATIVOS ###

print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)
print(3 == 4)
print(3 != 4)

print('Hola' < 'Python')
print('aaaa' >= 'abaa') # Ordenacion alfabetica ASCII
print(len('Hola') >= len('Python')) # Cuenta caracteres
print('Hola' <= 'Python')
print('Hola' == 'Python')
print('Hola' != 'Python')

### OPERADORES LOGICOS ###

print(3 > 4 and 'Hola' < 'Python')
print(3 > 4 or 'Hola' < 'Python')
print(not(3 > 4))

